package ca.mcmaster.se2aa4.island.team105.Drone;


public class RelativeCoordinate {

    private Integer x, y;

    public RelativeCoordinate(Integer x, Integer y) {
        this.x = x;
        this.y = y;
    }

    public Integer getX() {
        return x;
    }

    public Integer getY() {
        return y;
    }

    

}
